package Sampleranjith;

public class OddEven {		
	public static void main(String[] args) {
		for(int number=1; number<=100; number++)
		{
			if(number%2==0)
			{
				System.out.println("Even number:"+number);
			
			}
			else if(number%2==1)
			{
				System.out.println("Odd number"+number);
			}
			
		}
	}

	}


